function formatEventLabel(eventLabel) {
  return METER_EVENT_LABEL_MAP[eventLabel] || eventLabel;
}

function getEventSortInfo(eventLabel) {
  const explicitInfo = EVENT_SORT_INFO[eventLabel];
  if (explicitInfo) {
    return explicitInfo;
  }

  return {
    categoryRank: /XC/i.test(eventLabel) ? 1 : 0,
    distance: Number.POSITIVE_INFINITY,
    variantRank: 0
  };
}

function compareStats(leftStat, rightStat) {
  const leftInfo = getEventSortInfo(leftStat.event);
  const rightInfo = getEventSortInfo(rightStat.event);

  return (
    leftInfo.categoryRank - rightInfo.categoryRank ||
    leftInfo.distance - rightInfo.distance ||
    leftInfo.variantRank - rightInfo.variantRank
  );
}

function isRelayEntry(value) {
  return /\bDMR\b|\bSMR\b|\brelay\b|\b4x\s*mile\b|\b4xmile\b/i.test(value);
}

function matchHeader(line) {
  let match = line.match(/^(.*?)\s*-\s*(\d{4})(?:\s*\(([^)]+)\))?$/);

  if (!match) {
    // Support one known no-hyphen header style, but keep it strict so
    // accomplishment lines ending in event distances like "1500" do not
    // become fake runner entries.
    match = line.match(/^([A-Z][A-Za-z'.-]+(?: [A-Z][A-Za-z'.-]+){1,3})\s+(\d{4})(?:\s*\(([^)]+)\))?$/);
  }

  if (!match) {
    return null;
  }

  const name = normalizeTextValue(match[1]);
  const year = Number(match[2]);
  const qualifier = match[3] ? ` (${normalizeTextValue(match[3])})` : "";

  return {
    name,
    year,
    season: `${match[2]}${qualifier}`
  };
}

function parseStatLine(line, runnerName) {
  const normalized = normalizeTextValue(line);

  for (const eventLabel of EVENT_LABELS) {
    if (normalized.toLowerCase().startsWith(eventLabel.toLowerCase())) {
      const remainder = normalized.slice(eventLabel.length).trim().replace(/^:\s*/, "");
      const markMatch = remainder.match(/([0-9][0-9:.]*i?)$/i);

      if (!remainder || !markMatch) {
        return null;
      }

      const mark = markMatch[1];
      const isIndoor = /i$/i.test(mark);
      const note = remainder.slice(0, markMatch.index).trim();

      return {
        event: formatEventLabel(eventLabel),
        mark,
        displayMark: mark.replace(/i$/i, ""),
        isIndoor,
        note
      };
    }
  }

  const fixedEvent = FIXED_EVENT_MAP[runnerName]?.[normalized];
  if (fixedEvent) {
    return {
      event: formatEventLabel(fixedEvent),
      mark: normalized,
      displayMark: normalized,
      isIndoor: false,
      note: ""
    };
  }

  return null;
}

function slugify(text) {
  return text
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function finalizeRunner(runner) {
  const seenStats = new Set();
  const seenAchievements = new Set();

  const stats = runner.stats
    .filter((stat) => {
      if (isRelayEntry(stat.event) || isRelayEntry(stat.note || "")) {
        return false;
      }

      const key = `${stat.event}|${stat.mark}`.toLowerCase();
      if (seenStats.has(key)) {
        return false;
      }

      seenStats.add(key);
      return true;
    })
    .map((stat, index) => ({
      ...stat,
      sortIndex: index
    }))
    .sort(
      (leftStat, rightStat) =>
        compareStats(leftStat, rightStat) || leftStat.sortIndex - rightStat.sortIndex
    )
    .map(({ sortIndex, ...stat }) => stat);

  const achievements = runner.achievements
    .filter((achievement) => !isRelayEntry(achievement))
    .filter((achievement) => {
      const key = achievement.toLowerCase();
      if (seenAchievements.has(key)) {
        return false;
      }

      seenAchievements.add(key);
      return true;
    });

  return {
    ...runner,
    id: slugify(`${runner.name}-${runner.season}`),
    stats,
    achievements
  };
}

function parseRunnerData(rawText) {
  const lines = rawText
    .split(/\r?\n/)
    .map(normalizeLine)
    .filter(Boolean);

  const runners = [];
  let currentRunner = null;

  for (const line of lines) {
    const header = matchHeader(line);

    if (header) {
      if (currentRunner) {
        runners.push(finalizeRunner(currentRunner));
      }

      currentRunner = {
        ...header,
        seedRank: runners.length + 1,
        stats: [],
        achievements: []
      };
      continue;
    }

    if (!currentRunner) {
      continue;
    }

    const stat = parseStatLine(line, currentRunner.name);
    if (stat) {
      currentRunner.stats.push(stat);
    } else {
      currentRunner.achievements.push(formatAchievementText(line));
    }
  }

  if (currentRunner) {
    runners.push(finalizeRunner(currentRunner));
  }

  return runners;
}

window.RUNNER_DATA = parseRunnerData(RAW_RUNNER_TEXT);
